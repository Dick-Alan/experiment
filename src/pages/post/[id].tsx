const SinglePostPage: NextPage<{ id: string }> = ({ id }) => {
  const { data } = api.posts.getById.useQuery({
    id,
  });

  const [input, setInput] = useState("");
  const ctx = api.useContext();
  const { mutate, isLoading: isPosting } = api.posts.createComment.useMutation({
    onSuccess: () => {
      setInput("");
      void ctx.posts.getCommentsByPostId.invalidate();
    },
    onError: (e) => {
      const errorMessage = e.data?.zodError?.fieldErrors.content;
      if (errorMessage && errorMessage[0]) {
        toast.error(errorMessage[0]);
      } else {
        toast.error("Failed to post comment");
      }
    },
  });
  const user = useUser();
  const postId = id;
  const comments = api.posts.getCommentsByPostId.useQuery({
    postId,
  }).data;

  if (!data) return <div>404</div>;

  return (
    <>
      <Head>
        <title>{`${data.post.content ?? ""} - ${
          data.author.username ?? ""
        }`}</title>
        <meta name="description" content="Generated by create-t3-app" />
      </Head>
      <PageLayout>
        <Link href="/">
          <div className="col-span-2 m-4 flex h-[50px] w-[80px] rounded-md border-l-4 border-l-slate-700 bg-black p-1 hover:bg-slate-900">
            <div className="text-4xl">{"<"}</div>
          </div>
        </Link>
        <PostView {...data} />
      </PageLayout>
    </>
  );
};

